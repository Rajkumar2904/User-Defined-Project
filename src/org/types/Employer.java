package org.types;

public class Employer {
protected int i;
protected String s;
protected long l;
public int getI() {
	return i;
}
public void setI(int i) {
	this.i = i;
}
public String getS() {
	return s;
}
public void setS(String s) {
	this.s = s;
}
public long getL() {
	return l;
}
public void setL(long l) {
	this.l = l;
}

}
